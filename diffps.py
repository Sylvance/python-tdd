class Diffps(object):

    def add(self, arg1, arg2):
        """Return the sum of two arguments."""
        result1 = arg1 + arg2
        self.ans1 = result1
        return result1

    def multiply(self, arg1, arg2):
        """Return the product of two arguments."""
        result2 = arg1 * arg2
        self.ans2 = result2
        return result2
